[mysql (shell) 설정하기]

1) XAMPP Control panel 의 오른쪽 메뉴 중 shell 클릭
2) 제목줄이 있는 부분에서 오른쪽마우스 누르고 "속성" 클릭

3) 가장 왼쪽의 옵션 메뉴 클릭

	+ 가운데 영역 확인하기 (편집모드)
		: 빠른 편집모드 체크 해제하기 (오른쪽 마우스를 ㅏ용하기가 매우 불편함)

	+ 가장 하단 영역 확인하기
		: 레거시 콘솔 사용 체크 (모든 콘솔에 영향을 주기위해 체크)

4) 레이아웃 메뉴 클릭 / 버퍼크기, 높이부분을 7000 정도로 설정 
(9001로 되어있으면 굳이 건들지말기, 4~5000 정도 사용해도 문제없음)
	: 인식 (입력)되는 부분의 글자값이 부족하여 누락되는 경우가 발생함
	